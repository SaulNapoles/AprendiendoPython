# variables con valor True y mostrar resultado.
print('operación AND sobre un valores True.\n')
booleano1 = True
booleano2 = True
print(booleano1 and booleano2)

print('\n')

# operación AND sobre un valor True y otro False
print('operación AND sobre un valor True y otro False.\n')
booleano1 = True
booleano2 = False
print(booleano1 and booleano2)

print('\n')

# operación AND sobre dos valores False
print('operación AND sobre dos valores False.\n')
booleano1 = False
booleano2 = False
print(booleano1 and booleano2)

print('\n')

# Operación OR sobre dos valores True
print('Operación OR sobre dos valores True.\n')
booleano1 = True
booleano2 = True
print(booleano1 or booleano2)

print('\n')

# Operación OR sobre un valor True y otro valor False
print('Operación OR sobre un valor True y otro valor False.\n')
booleano1 = True
booleano2 = False
print(booleano1 or booleano2)

print('\n')
# Operación OR sobre dos valores False
print('Operación OR sobre dos valores False.\n')
booleano1 = False
booleano2 = False
print(booleano1 or booleano2)

print('\n')

